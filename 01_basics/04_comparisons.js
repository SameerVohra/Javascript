console.log("COMPARISON OF SAME DATATYPES");
console.log(2 > 4);
console.log(2 < 4);
console.log(2 == 4);
console.log(2 != 4);

let str1 = "abc";
let str2 = "bcd";

console.log(str1 == str2);
console.log(str1 != str2);

console.log("COMPARISON OF DIFFERENT DATATYPES");
console.log("1" > 3);
console.log("02" >= 2);

console.log("COMPARISON WITH NULL");
console.log(null > 0);
console.log(null >= 0);
console.log(null == 0);

console.log("COMPARISON WITH UNDEFINED");
console.log(undefined >= 0);
console.log(undefined == 0);
console.log(undefined < 0);

console.log("STRICT CHECK(===)");
console.log("33" === 33);
