//While Loop

let i = 0;
while (i <= 10) {
  console.log(i);
  i += 2;
}

let myArr = ["Flash", "Batman", "Superman"];
let ind = 0;
while (ind < myArr.length) {
  console.log(myArr[ind]);
  ind++;
}

//DO WHILE

let score = 0;
do {
  console.log(`Score is ${score}`);
  score++;
} while (score <= 10);
